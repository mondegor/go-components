-- 1. один процесс ставит сообщения в очередь message_inbox

-- for insert, read, delete (фоном)
CREATE TABLE printshop_global.message_inbox (
    message_id int8 NOT NULL GENERATED BY DEFAULT AS IDENTITY CONSTRAINT pk_message_inbox PRIMARY KEY,
    message_key uuid NOT NULL,
    message_topic character varying(128) NOT NULL,
    message_payload bytea NOT NULL,
    message_payload_json jsonb NOT NULL,
    retry_attempts int2 NOT NULL,
    retry_delayed int2 NOT NULL,
    dt_start timestamp with time zone NOT NULL DEFAULT NOW(),
    created_at timestamp with time zone NOT NULL DEFAULT NOW()
);


-- for insert, read, delete (фоновый)
CREATE TABLE printshop_global.item_queue_body (
    item_id int8 NOT NULL CONSTRAINT pk_item_queue_body PRIMARY KEY,
    item_key character varying(64) NOT NULL, -- can by ID + VERSION
    retry_attempts int2 NOT NULL,
    created_at timestamp with time zone NOT NULL DEFAULT NOW()
);

CREATE UNIQUE INDEX uk_message_inbox_message_key ON printshop_global.message_inbox (message_key);
CREATE INDEX ix_message_inbox_dt_start ON printshop_global.message_inbox (dt_start);

-- 2. один процесс (N воркеров) ставит в очередь message_inbox_processing (читает из message_inbox) и выполняет эти процессы,
--    после успешного выполнения удаляет из message_inbox_processing и пишет в message_inbox_success,
--    если произошла ошибка, то пишет в message_inbox_error, удаляет из message_inbox_processing и пишет в message_inbox_retry (если попытки остались)
-- 3. один процесс следит, в message_inbox_processing updated_at было небольшим,
--    иначе вылет по таймауту и запись в message_inbox_retry (если не закончились попытки) и в message_inbox_error

-- for insert, read, delete
CREATE TABLE printshop_global.message_inbox_processing (
    message_id int8 NOT NULL CONSTRAINT pk_message_inbox_processing PRIMARY KEY,
    remaining_attempts int2 NOT NULL,
    updated_at timestamp with time zone NOT NULL DEFAULT NOW()
);

-- 4. один процесс удаляет из message_inbox_retry (согласно прописанной паузе) и ставит в очередь message_inbox_processing (с уменьшением 1 попытки, тут попытки не должны закончиться)
--    и выполняет эти процессы, после успешного выполнения удаляет из message_inbox_processing и пишет в message_inbox_success

-- for insert, read, delete
CREATE TABLE printshop_global.message_inbox_retry (
    message_id int8 NOT NULL CONSTRAINT pk_message_inbox_retry PRIMARY KEY,
    remaining_attempts int2 NOT NULL,
    updated_at timestamp with time zone NOT NULL DEFAULT NOW()
);

-- 5. один процесс чистит сообщения об ошибках по created_at

-- for insert, read, delete (фоном)
CREATE TABLE printshop_global.message_inbox_error (
    error_message text NOT NULL,
    message_id int8 NOT NULL,
    current_attempt int2 NOT NULL,
    created_at timestamp with time zone NOT NULL DEFAULT NOW()
);

CREATE INDEX ix_message_inbox_error_message_id ON printshop_global.message_inbox_error (message_id);
CREATE INDEX ix_message_inbox_error_created_at ON printshop_global.message_inbox_error (created_at);

-- for insert, read, delete
CREATE TABLE printshop_global.message_inbox_success (
    message_id int8 NOT NULL CONSTRAINT pk_message_inbox_success PRIMARY KEY,
    updated_at timestamp with time zone NOT NULL DEFAULT NOW()
);

-- 6. один процесс выбирает данные из message_inbox + message_inbox_success + message_inbox_error  и добавляет в message_inbox_archive
--    и удаляет из message_inbox, message_inbox_success и message_inbox_error
-- 7. один процесс удаляет архивные сообщения (по created_at)

-- only for insert (фоном), read, delete (фоном)
CREATE TABLE printshop_global.message_inbox_archive (
    message_key uuid NOT NULL CONSTRAINT pk_message_inbox_archive PRIMARY KEY,
    message_topic character varying(128) NOT NULL,
    message_payload bytea NOT NULL,
    message_payload_json jsonb NOT NULL,
    retry_attempts int2 NOT NULL,
    spent_attempts int2 NOT NULL,
    last_error text NOT NULL,
    message_status int2 NOT NULL, -- 1=SUCCESS, 2=ERROR
    started_at timestamp with time zone NOT NULL DEFAULT NOW(),
    created_at timestamp with time zone NOT NULL DEFAULT NOW(),
    updated_at timestamp with time zone NOT NULL DEFAULT NOW()
);

CREATE INDEX ix_message_inbox_archive_created_at ON printshop_global.message_inbox_archive (created_at);





