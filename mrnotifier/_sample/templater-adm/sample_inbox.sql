CREATE TABLE `erp_messages` (
                                `message_id` mediumint(8) unsigned NOT NULL AUTO_INCREMENT,
    `datetime_sent` datetime NOT NULL DEFAULT '0000-00-00 00:00:00',
    `from_user_id` mediumint(8) unsigned NOT NULL DEFAULT '0',
    `message_type` enum('private','common','system') NOT NULL DEFAULT 'private',
    `message_priority` enum('normal','extra') NOT NULL DEFAULT 'normal',
    `message_text` text NOT NULL,
    PRIMARY KEY (`message_id`),
    KEY `datetime_sent` (`datetime_sent`),
    KEY `from_user_id` (`from_user_id`)
    ) ENGINE=MyISAM AUTO_INCREMENT=207412 DEFAULT CHARSET=cp1251;


CREATE TABLE `erp_messages_groups` (
                                       `group_id` tinyint(3) unsigned NOT NULL AUTO_INCREMENT,
    `group_name` char(50) NOT NULL DEFAULT '',
    `is_system` tinyint(1) unsigned NOT NULL DEFAULT '0',
    `order_field` tinyint(3) unsigned NOT NULL DEFAULT '0',
    PRIMARY KEY (`group_id`),
    UNIQUE KEY `group_name` (`group_name`),
    KEY `order_field` (`order_field`)
    ) ENGINE=MyISAM AUTO_INCREMENT=11 DEFAULT CHARSET=cp1251;

