package validate

import (
	"regexp"
)

var regexpLang = regexp.MustCompile(`^[a-zA-Z]{2,3}([-_][a-zA-Z]{2})?$`)

// Lang - сообщает о соответствии указанного значения формату языка (локали) пользователя:
// двух- или трёхбуквенный код языка и необязательный код региона (напр. ru, ru-RU, en_US).
//
// Канонический вид не требуется: регистр и разделитель произвольны ("ru_RU", "en-us"), т.к.
// клиенты присылают язык в разных записях, а к единому виду его приводит резолвер приложения
// (см. mrauth/service/lang.Resolver) - именно его результат и сохраняется. Поэтому здесь
// проверяется только форма записи, чтобы отсеять заведомый мусор до обращения к резолверу.
//
// Поддерживается ли язык приложением, здесь не проверяется: локализатор подбирает ближайший
// из доступных, а при отсутствии подходящего берёт язык по умолчанию.
func Lang(value string) bool {
	return regexpLang.MatchString(value)
}
