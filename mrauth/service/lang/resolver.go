package lang

import (
	"github.com/mondegor/go-core/mrlocale"
	"golang.org/x/text/language"
)

type (
	// Resolver - приводит язык, присланный пользователем, к языку,
	// поддерживаемому приложением.
	Resolver struct {
		pool localePool
	}

	// localePool - подбирает локализатор для наиболее близкого из поддерживаемых языков.
	localePool interface {
		Localizer(langs ...language.Tag) *mrlocale.Localizer
	}
)

// New - создаёт объект Resolver.
func New(pool localePool) *Resolver {
	return &Resolver{
		pool: pool,
	}
}

// Resolve - приводит язык к тому виду, в котором его отдаёт локализатор приложения:
// например, "ru" и "ru_RU" сводятся к "ru-RU", а неподдерживаемый язык - к языку
// по умолчанию. Возвращаемое значение всегда является одним из поддерживаемых языков.
//
// Язык, присланный клиентом, может быть корректным, но приложением не поддерживаемым:
// список языков ограничен настройками приложения. Поэтому такой язык не отвергается,
// а заменяется на ближайший поддерживаемый (по правилам language.Matcher), а при
// отсутствии подходящего - на язык по умолчанию.
//
// Негодная запись языка тоже не отвергается: локаль по умолчанию всё равно является
// корректным поддерживаемым языком, поэтому клиент получает работающее значение,
// а не ошибку.
//
// Пригодность возвращаемого значения для хранения здесь отдельно не проверяется: возвращается
// язык из списка, настроенного хостом. Гарантия того, что язык из этого списка помещается
// в колонку, обеспечивается проверкой wire/mrauth/config.ValidateLanguages, но вызывает её
// хост при загрузке конфигурации - библиотека сделать это за него не может. Хост, пропустивший
// проверку, получит отказ на вставке пользователя (напр. "zh-Hans" не влезет в lang_code).
func (sv *Resolver) Resolve(lang string) string {
	tag, err := language.Parse(lang)
	if err != nil {
		return sv.pool.Localizer().Language()
	}

	return sv.pool.Localizer(tag).Language()
}
